from ..exercise_2 import price_deltas


def test_prices_delta() -> None:
    prices: list[tuple[int, int | None]] = price_deltas(123, 9)

    expected: list[tuple[int, int | None]] = [
        (0, -3),
        (6, 6),
        (5, -1),
        (4, -1),
        (4, 0),
        (6, 2),
        (4, -2),
        (4, 0),
        (2, -2),
    ]

    assert prices == expected
